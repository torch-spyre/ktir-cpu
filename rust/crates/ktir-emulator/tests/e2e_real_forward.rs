/// Open a repo's safetensors as a `name -> bf16 f16-bytes` resolver. Handles
/// single-file (`model.safetensors`) and sharded (`*.index.json`) layouts.
struct Weights {
    /// tensor name -> f16 bytes already converted from bf16 (verbatim PyTorch `[out,in]`,
    /// bound zero-copy — the program's `matmul_transpose_b` reads this layout directly).
    f16: HashMap<String, Vec<u8>>,
}

impl Weights {
    /// Returns `None` (skip the test) if the repo can't be fetched (offline /
    /// HF down) — so a network-less box degrades gracefully rather than failing
    /// spuriously; a box WITH network exercises the real path.
    fn fetch(repo: &str) -> Option<Weights> {
        let api = hf_hub::api::sync::Api::new().ok()?;
        let model = api.model(repo.to_string());

        // Collect the safetensors shard filenames.
        let shard_files: Vec<String> = match model.get("model.safetensors.index.json") {
            Ok(idx) => {
                let j: serde_json::Value =
                    serde_json::from_slice(&std::fs::read(idx).ok()?).ok()?;
                let mut set = std::collections::BTreeSet::new();
                for v in j["weight_map"].as_object()?.values() {
                    set.insert(v.as_str()?.to_string());
                }
                set.into_iter().collect()
            }
            Err(_) => vec!["model.safetensors".to_string()],
        };

        let t_dl = std::time::Instant::now();
        let mut blobs = Vec::new();
        for f in &shard_files {
            let path = model.get(f).ok()?; // cached → ~free; cache-miss → network
            blobs.push(std::fs::read(path).ok()?);
        }
        let dl_s = t_dl.elapsed().as_secs_f64();
        let dl_mb = blobs.iter().map(|b| b.len()).sum::<usize>() as f64 / 1e6;
        // Eagerly convert every tensor's bf16 bytes -> f16 bytes (the stick
        // layout). bf16 is the on-disk dtype for these models.
        let t_cv = std::time::Instant::now();
        let mut f16 = HashMap::new();
        // Consume each shard by value and drop it right after converting, so the
        // ~2.5 GB of bf16 bytes are freed instead of staying resident behind the
        // f16 map for the whole forward. (Keeping them alive was the e2e's peak-RSS
        // hog → swap thrashing on the 7 GB macOS runner; not an emulator issue.)
        for b in blobs.drain(..) {
            let st = safetensors::SafeTensors::deserialize(&b).ok()?;
            for (name, view) in st.tensors() {
                let raw = view.data();
                let n = raw.len() / 2;
                // bf16 LE bytes -> f16 LE bytes via the crate codec.
                let as_f16 = ktir_emulator::codec::bf16_to_f16(raw, n);
                f16.insert(name.to_string(), as_f16);
            }
        }
        let cv_s = t_cv.elapsed().as_secs_f64();
        eprintln!(
            "  [Weights::fetch {repo}: get+read {dl_mb:.0} MB in {dl_s:.1}s, bf16->f16 convert {cv_s:.1}s]"
        );
        Some(Weights { f16 })
    }

    /// Verbatim f16 bytes for `<disk>.weight` in PyTorch `[out,in]` order — bound
    /// zero-copy, NO transpose. The KTIR program is responsible for contracting the
    /// correct axis (via `matmul_transpose_b`). Tied lm_head falls back to
    /// `embed_tokens.weight`.
    fn weight(&self, disk: &str) -> Option<&[u8]> {
        if let Some(v) = self.f16.get(&format!("{disk}.weight")) {
            return Some(v);
        }
        if disk == "lm_head" {
            return self
                .f16
                .get("model.embed_tokens.weight")
                .map(|v| v.as_slice());
        }
        None
    }
}

/// Deterministic synthetic activation for a non-weight source, used only as a
/// pre-generation fallback when a vendored `t<id>.f16.gz` is absent. RoPE-valid
/// (cos=1, sin=0 = identity rotation) so the forward stays finite.
fn synth(role: &str, n: usize) -> Vec<f32> {
    match role {
        "cos" => vec![1.0; n],
        "sin" => vec![0.0; n],
        // input activation / KV-cache prefixes: small bounded deterministic.
        _ => (0..n).map(|i| ((i % 17) as f32 - 8.0) * 0.01).collect(),
    }
}

/// Build the source args (keyed `t{id}`). Real f16 weights from HF; the runtime
/// activations (input activation, RoPE cos/sin, KV prefix) come from the VENDORED
/// `t<id>.f16.gz` the generator wrote from the real model forward, so production is
/// fed exactly what the `golden.f16.gz` was computed against. (A missing file falls
/// back to a deterministic synth so the loader still runs pre-generation.)
fn build_args(dir: &Path, p: &Program, w: &Weights) -> Vec<(String, Arg)> {
    let mut args = Vec::new();
    let mut ids: Vec<u64> = p.is_source.iter().copied().collect();
    if let Some(m) = p.mask_id
        && !ids.contains(&m)
    {
        ids.push(m);
    }
    ids.sort_unstable();
    let m_valid = p
        .src_meta
        .iter()
        .find(|(_, (role, _))| role == "embed")
        .map(|(id, _)| p.shape[id].0)
        .unwrap_or(1);
    for id in ids {
        let (rows, cols) = p.shape[&id];
        let n = rows * cols;
        let key = format!("t{id}");
        let vendored = dir.join(format!("t{id}.f16.gz"));

        // Weight sources: always from HF (verbatim f16 bytes).
        if let Some((_, Some(d))) = p.src_meta.get(&id) {
            let bytes = w
                .weight(d)
                .unwrap_or_else(|| panic!("HF weight {d:?} (t{id}) not found in repo"));
            assert_eq!(
                bytes.len(),
                n * 2,
                "weight {d} (t{id}) numel {} != {n}",
                bytes.len() / 2
            );
            args.push((
                key,
                Arg::TensorBytes {
                    data: bytes.to_vec(),
                    shape: vec![rows, cols],
                    dtype: DType::F16,
                },
            ));
            continue;
        }

        // Runtime activations: prefer the vendored real tensor (generated from the
        // model forward); else fall back to a deterministic synth.
        let data = if vendored.is_file() {
            let v = read_f16_gz(&vendored);
            assert_eq!(v.len(), n, "vendored t{id} len {} != {n}", v.len());
            v
        } else if Some(id) == p.mask_id {
            // The mask applies to the 64-position prefix KV CACHE, which is EMPTY
            // for a fresh forward (the current token(s) enter via a separate fresh
            // K/V path, not through this mask). So mask ALL prefix positions (f16-min
            // additive ≈ -inf) — attention then uses only the fresh tokens.
            let _ = m_valid;
            vec![-65504.0; n]
        } else {
            let role = p
                .src_meta
                .get(&id)
                .map(|(r, _)| r.as_str())
                .unwrap_or("weight");
            match role {
                "prefix_k" | "prefix_v" => vec![0.0; n],
                _ => synth(role, n),
            }
        };
        args.push((
            key,
            Arg::Tensor {
                data,
                shape: vec![rows, cols],
                dtype: DType::F16,
            },
        ));
    }
    args
}

/// Production path: the real serving API — fuse into ordered segments and run
/// each (fused [1,1] GPU/AMX offload + native head-parallel attention).
///
/// `last_token`: when true (and on Metal), the final lm_head projection computes
/// ONLY the last token's logits (`[1, vocab]`) — the production generation default,
/// since autoregressive sampling reads only the last position. The returned buffer
/// is still `[m, vocab]`-sized but only its last row is populated. When false it
/// computes ALL m rows (the comprehensive all-rows correctness path).
fn forward_production(p: &Program, args: &[(String, Arg)], last_token: bool) -> Vec<f32> {
    let refs: Vec<(&str, Arg)> = args.iter().map(|(n, a)| (n.as_str(), a.clone())).collect();
    let key = format!("t{}", p.result_id);
    // RESIDENT (weights marshaled ONCE into one persistent HBM, kernels chained
    // on-device, no per-segment host round-trip) is the fast path ON METAL — it
    // avoids re-encoding/decoding ~2.5 GB per segment × ~178 segments per pass.
    //
    // OFF METAL (Linux CI) there is no GPU HBM to stay resident in and the GEMM
    // offload can't reach NAX/AMX, so the resident path runs the K-loop GEMMs
    // through the interpreter and is ~2× SLOWER than the segmented path there
    // (it doubled the Linux e2e: 11 → 21 min). The segmented path routes each
    // matmul tile to OpenBLAS, so use it off-Metal. Both reproduce the golden.
    #[cfg(metal)]
    let out = {
        let mut exec = ktir_emulator::resident::ResidentExecutor::new(p.module.clone(), &p.spec)
            .expect("build resident executor");
        exec.set_sources(&refs).expect("marshal sources");
        exec.set_last_token_only(last_token);
        exec.run(&[&key]).expect("execute_resident")
    };
    #[cfg(not(metal))]
    let out = {
        let _ = last_token; // last-token pruning is the Metal resident path only
        ktir_emulator::segmented::execute_segmented(&p.module, &p.spec, &refs, &[&key])
            .expect("execute_segmented")
    };
    out.get(&key).expect("result").data.clone()
}

/// Run a real model forward and assert the production output matches the
/// REAL-MODEL golden (transformers logits, vendored as `golden.f16.gz` by
/// `gen_golden.py`). Skips if the golden isn't present (not generated) or the repo
/// can't be fetched (offline). The primary check is the next-token argmax of the
/// last row; `max_abs` is reported and bounded (f16 over many layers vs an f32
/// golden drifts, so the band is loose — argmax is the real assertion).
fn real_forward_golden(fixture: &str, repo: &str) {
    let dir = fixture_dir(fixture);
    if !dir.join("golden.f16.gz").is_file() {
        eprintln!(
            "{fixture}: no vendored golden.f16.gz (run tests/fixtures/gen_golden.py) — skipping"
        );
        return;
    }
    let p = load_program(&dir);
    let t_fetch = std::time::Instant::now();
    let Some(w) = Weights::fetch(repo) else {
        eprintln!("{fixture}: could not fetch {repo} (offline?) — skipping");
        return;
    };
    let fetch_s = t_fetch.elapsed().as_secs_f64();
    let args = build_args(&dir, &p, &w);
    let t_fwd = std::time::Instant::now();
    // ALL-ROWS path: compute every token position so the per-row argmax check below
    // validates the whole forward (a stricter probe than production needs). The
    // last-token production default is validated separately by
    // `real_forward_golden_last_token`.
    let prod = forward_production(&p, &args, false);
    let fwd_s = t_fwd.elapsed().as_secs_f64();
    eprint!("{}", ktir_emulator::interpreter::profile_report()); // KTIR_PROFILE=1
    let golden = read_f16_gz(&dir.join("golden.f16.gz"));

    assert_eq!(
        prod.len(),
        golden.len(),
        "{fixture}: result {} != golden {}",
        prod.len(),
        golden.len()
    );
    assert!(
        prod.iter().all(|x| x.is_finite()),
        "{fixture}: non-finite production output"
    );

    let (m, vocab) = p.shape[&p.result_id];
    assert_eq!(m * vocab, prod.len(), "{fixture}: result shape {m}x{vocab}");
    // The emulator's next-token pick must be one the real model also ranked at
    // the very top: its GOLDEN logit must be within `tol` of the golden max.
    // Exact argmax is too strict when the model's top tokens are near-tied — the
    // smollm2 prompt's top-5 sit within ~0.5 logits (a 0.16 margin) — so harmless
    // cross-platform f16 drift (~0.3 on macOS Accelerate, ~1.0 on Linux OpenBLAS,
    // over ~30 f16 layers vs an f32 golden) flips the pick to an equally-valid
    // neighbour. `tol` must clear the worst-case gap drift alone can open:
    // pick = argmax(prod) ⇒ golden[pick] ≥ golden_max − 2·max_abs, so the gap can
    // reach 2·max_abs (≈2.1 for the observed Linux max_abs ≈ 1.05). 3.0 covers it
    // with headroom; a genuinely broken forward has a far larger max_abs and still
    // fails; a confident model (llama) keeps this ~exact (its pick gap is ~0).
    const ARGMAX_LOGIT_TOL: f32 = 3.0;
    let mut argmax_hits = 0usize;
    for r in 0..m {
        let row = r * vocab..(r + 1) * vocab;
        let g = &golden[row.clone()];
        let pred = argmax(&prod[row]);
        let g_max = g.iter().copied().fold(f32::NEG_INFINITY, f32::max);
        if g_max - g[pred] <= ARGMAX_LOGIT_TOL {
            argmax_hits += 1;
        }
    }
    let max_abs = prod
        .iter()
        .zip(&golden)
        .map(|(a, b)| (a - b).abs())
        .fold(0.0f32, f32::max);
    eprintln!(
        "{fixture} via {repo}: [{m},{vocab}] logits, {argmax_hits}/{m} rows pick a top token \
         (within {ARGMAX_LOGIT_TOL} logits of the model's best), max abs diff {max_abs:.4} \
         [weights {fetch_s:.1}s, forward {fwd_s:.1}s]"
    );
    assert_eq!(
        argmax_hits, m,
        "{fixture}: next-token prediction is not within {ARGMAX_LOGIT_TOL} logits of the real model's top token"
    );
    // f16 over ~30 layers vs an f32 golden drifts more on Linux OpenBLAS (~1.05)
    // than on macOS Accelerate (~0.27) — different f32 accumulation order. The
    // band is loose because the per-row argmax above is the meaningful check; this
    // only catches a grossly broken forward (max_abs would be far larger / NaN).
    assert!(
        max_abs < 2.0,
        "{fixture}: logits diverge from golden by {max_abs}"
    );
}

