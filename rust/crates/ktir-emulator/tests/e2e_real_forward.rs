//! REAL-MODEL end-to-end — hermetic w.r.t. the scratchy `.cache`.
//!
//! Each test runs a real forward of a real model: the KTIR *program* is vendored
//! in-repo (`tests/fixtures/<model>/` — `manifest.json` + `node*.mlir`, no
//! weights), and the *weights* are fetched from a PUBLIC HuggingFace repo (no
//! `HF_TOKEN`) via `hf-hub` and read with the `safetensors` crate. Nothing reads
//! `~/.cache/cudaforge`.
//!
//! Correctness is checked against a REAL-MODEL GOLDEN: the production path
//! (`execute_segmented` — fuse + GPU/AMX offload + head-parallel attention) must
//! reproduce the `transformers` logits (vendored as `golden.f16.gz`, regenerated by
//! `tests/fixtures/gen_golden.py`). The per-row next-token argmax must match the
//! real model; `max_abs` is reported and loosely bounded (f16 over many layers vs
//! an f32 golden drifts). This catches fusion/offload/marshal/weight-layout bugs.
//!
//! Weights are PyTorch linear weights (`[out,in]`), bound VERBATIM / zero-copy as
//! f16 — NO transpose at load. The KTIR program contracts the correct axis itself
//! (`matmul_transpose_b`). Runtime activations the program expects but that aren't
//! HF weights — the input activation (`role:embed`), RoPE `cos`/`sin`, the (empty)
//! KV-cache `prefix_k`/`prefix_v`, and the attention mask — come from the vendored
//! `t<id>.f16.gz` the generator wrote from the same model forward.
//!
//! NOT `#[ignore]` / NOT env-gated: runs in default `cargo test`. Weights are
//! fetched from HF (content-addressed, cached). Prefill (m>1) is `cfg(metal)` —
//! the fused [1,1] segments need the offload for full-M reconstruction.

