/// LAST-TOKEN golden: the PRODUCTION generation default — the lm_head computes only
/// the LAST token's logits — must still pick the real model's next token. This is the
/// correctness gate that justifies last-token mode being ON by default in the resident
/// production path (`execute_resident`): identical next-token prediction, ~1/m the
/// lm_head work. Mirrors [`real_forward_golden`] but asserts on the LAST row only (the
/// other rows are intentionally not computed in this mode). Prefill-only / `cfg(metal)`
/// (the rewrite is the resident GPU path; decode is m=1 so all-rows == last-token).
#[cfg(metal)]
fn real_forward_golden_last_token(fixture: &str, repo: &str) {
    let dir = fixture_dir(fixture);
    if !dir.join("golden.f16.gz").is_file() {
        eprintln!("{fixture}: no vendored golden.f16.gz — skipping");
        return;
    }
    let p = load_program(&dir);
    let Some(w) = Weights::fetch(repo) else {
        eprintln!("{fixture}: could not fetch {repo} (offline?) — skipping");
        return;
    };
    let args = build_args(&dir, &p, &w);
    // LAST-TOKEN production path: only row m-1 of the logits is computed.
    let prod = forward_production(&p, &args, true);
    let golden = read_f16_gz(&dir.join("golden.f16.gz"));
    let (m, vocab) = p.shape[&p.result_id];
    assert_eq!(prod.len(), golden.len(), "{fixture}: result/golden length");
    // Only the LAST row is computed in last-token mode — check exactly that row.
    const ARGMAX_LOGIT_TOL: f32 = 3.0;
    let r = m - 1;
    let row = r * vocab..(r + 1) * vocab;
    let pred_row = &prod[row.clone()];
    assert!(
        pred_row.iter().all(|x| x.is_finite()),
        "{fixture}: non-finite last-token logits"
    );
    let g = &golden[row];
    let pred = argmax(pred_row);
    let g_max = g.iter().copied().fold(f32::NEG_INFINITY, f32::max);
    let max_abs = pred_row
        .iter()
        .zip(g)
        .map(|(a, b)| (a - b).abs())
        .fold(0.0f32, f32::max);
    eprintln!(
        "{fixture} via {repo} [LAST-TOKEN]: last row [{vocab}] picks {} (golden gap {:.4}), \
         max abs diff {max_abs:.4}",
        pred,
        g_max - g[pred]
    );
    assert!(
        g_max - g[pred] <= ARGMAX_LOGIT_TOL,
        "{fixture}: last-token next-token prediction is not within {ARGMAX_LOGIT_TOL} logits \
         of the real model's top token"
    );
    assert!(
        max_abs < 2.0,
        "{fixture}: last-token last-row logits diverge from golden by {max_abs}"
    );
}

