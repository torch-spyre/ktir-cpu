// LAST-TOKEN-ONLY MODE — correctness + perf.
//
// Autoregressive generation only needs the LAST token's logits to pick the next
// token; the other m-1 prefill rows of the lm_head projection are pure waste.
// `ResidentExecutor::set_last_token_only(true)` rewrites the final result GEMM to
// compute only output row m-1, turning the [m,vocab] lm_head into a [1,vocab] one.
// ---------------------------------------------------------------------------

/// CORRECTNESS: the last-token-only logits must equal the full all-rows path's
/// LAST row (within f16 tolerance and with the SAME argmax). Hermetic — fixtures +
/// public HF weights, the same harness the `*_real_forward` golden tests use.
#[cfg(metal)]
fn last_token_equiv(fixture: &str, repo: &str) {
    let dir = fixture_dir(fixture);
    if !dir.join("manifest.json").is_file() {
        eprintln!("{fixture}: no fixture manifest — skipping");
        return;
    }
    let p = load_program(&dir);
    let Some(w) = Weights::fetch(repo) else {
        eprintln!("{fixture}: could not fetch {repo} (offline?) — skipping");
        return;
    };
    let args = build_args(&dir, &p, &w);
    let refs: Vec<(&str, Arg)> = args.iter().map(|(n, a)| (n.as_str(), a.clone())).collect();
    let key = format!("t{}", p.result_id);
    let (m, vocab) = p.shape[&p.result_id];

    // FULL all-rows path.
    let mut exec = ktir_emulator::resident::ResidentExecutor::new(p.module.clone(), &p.spec)
        .expect("build resident executor");
    exec.set_sources(&refs).expect("marshal weights once");
    let full = exec
        .run(&[&key])
        .expect("full run")
        .get(&key)
        .expect("result")
        .data
        .clone();

    // LAST-TOKEN-only path (same executor + resident weights — no re-marshal).
    exec.set_last_token_only(true);
    let last = exec
        .run(&[&key])
        .expect("last-token run")
        .get(&key)
        .expect("result")
        .data
        .clone();

    // Compare the last-token output's LAST row to the full path's LAST row.
    let row = (m - 1) * vocab..m * vocab;
    let f = &full[row.clone()];
    let l = &last[row];
    let max_abs = f
        .iter()
        .zip(l)
        .map(|(a, b)| (a - b).abs())
        .fold(0.0f32, f32::max);
    let arg_full = argmax(f);
    let arg_last = argmax(l);
    eprintln!(
        "{fixture} last-token-only: last-row max-abs vs full {max_abs:.4}, \
         argmax full={arg_full} last={arg_last} (m={m}, vocab={vocab})"
    );
    assert!(
        last.iter().all(|x| x.is_finite()),
        "{fixture}: non-finite last-token output"
    );
    assert_eq!(
        arg_full, arg_last,
        "{fixture}: last-token argmax differs from the full path's last row"
    );
    // f16 GEMM rounding only — the SAME activation row through the SAME weight, so
    // the band is tight (a few e-1 like the golden tests' max_abs).
    assert!(
        max_abs <= 0.5,
        "{fixture}: last-token last-row diverges from the full path by {max_abs}"
    );
}

