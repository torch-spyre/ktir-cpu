// ---------------------------------------------------------------------------
// HERMETIC E2E PERF — whole-model ms/pass through the production RESIDENT path
// (`resident::ResidentExecutor`: weights marshaled ONCE into one persistent HBM,
// segments chained on-device, NO per-pass re-marshal), on the SAME vendored
// fixtures + public HF weights the golden tests use — NO `~/.cache/cudaforge`
// bundle. This is the RUST side of PERFORMANCE.md's "E2E whole-model" table; the
// PYTHON side is `tests/fixtures/bench_e2e_hermetic.py` (the per-node reference
// interpreter on the same fixtures + HF weights). best-of-N median, one warm-up
// pass excluded; ITERS env overrides the pass count (default 5). `#[ignore]`
// (heavy + needs HF weights); run: `--release --ignored --nocapture --test-threads=1`.
// ---------------------------------------------------------------------------
#[cfg(metal)]
fn time_resident_e2e(fixture: &str, repo: &str) {
    let dir = fixture_dir(fixture);
    if !dir.join("manifest.json").is_file() {
        eprintln!("{fixture}: no fixture manifest — skipping");
        return;
    }
    let p = load_program(&dir);
    let Some(w) = Weights::fetch(repo) else {
        eprintln!("{fixture}: could not fetch {repo} (offline?) — skipping");
        return;
    };
    let args = build_args(&dir, &p, &w);
    let refs: Vec<(&str, Arg)> = args.iter().map(|(n, a)| (n.as_str(), a.clone())).collect();
    let key = format!("t{}", p.result_id);
    let iters: usize = std::env::var("ITERS")
        .ok()
        .and_then(|s| s.parse().ok())
        .unwrap_or(5);

    // Resident contract: build the executor + marshal weights ONCE, OUTSIDE the
    // timed loop; the multi-pass loop re-uploads NOTHING.
    let mut exec = ktir_emulator::resident::ResidentExecutor::new(p.module.clone(), &p.spec)
        .expect("build resident executor");
    exec.set_sources(&refs).expect("marshal weights once");
    exec.run(&[&key]).expect("warmup"); // excluded

    let mut times: Vec<f64> = Vec::with_capacity(iters);
    for _ in 0..iters {
        let t = std::time::Instant::now();
        exec.run(&[&key]).expect("timed resident run");
        times.push(t.elapsed().as_secs_f64() * 1e3);
    }
    times.sort_by(|a, b| a.partial_cmp(b).unwrap());
    let median = times[times.len() / 2];
    let (m, _vocab) = p.shape[&p.result_id];
    eprintln!("{fixture} e2e (Rust RESIDENT): {median:.1} ms/pass  (m={m}, {iters} passes)");
    eprint!("{}", ktir_emulator::interpreter::profile_report()); // KTIR_PROFILE=1
}

/// REGRESSION GUARD for the multi-step decode path the single-pass golden tests do
/// NOT cover: scratchy's vllm loop calls `set_sources` + `run` every token, so the
/// resident weight cache must survive across `set_sources` calls AND never serve a
/// stale buffer. This builds ONE executor, runs, calls `set_sources` again (as the
/// decode loop does), runs again, and asserts the output equals a FRESH executor's
/// output for the same sources. A weight-cache bug (keeping a stale/wrong buffer
/// across `set_sources`) makes the second run diverge — invisible to the one-shot
/// golden. Default `#[test]` (cfg(metal)); small fixture for speed.
#[cfg(metal)]
#[test]
fn resident_multistep_set_sources_stable() {
    let dir = fixture_dir("smollm2-135m");
    if !dir.join("manifest.json").is_file() || !dir.join("golden.f16.gz").is_file() {
        eprintln!("smollm2-135m: fixture absent — skipping");
        return;
    }
    let p = load_program(&dir);
    let Some(w) = Weights::fetch("HuggingFaceTB/SmolLM2-135M") else {
        eprintln!("offline — skipping");
        return;
    };
    let args = build_args(&dir, &p, &w);
    let key = format!("t{}", p.result_id);
    let read = |out: &std::collections::HashMap<String, ktir_emulator::interpreter::Output>| {
        out.get(&key).expect("result").data.clone()
    };
    // A PERTURBED copy of the input activation (the `embed` source, t0): a different
    // "token". Decode threads a new one each step, so the kept constant weights must
    // produce the SAME result for it as a fresh executor — while the KV cache (a
    // forward-written tid) is correctly re-read, not served stale.
    let perturb = |a: &Arg| -> Arg {
        match a {
            Arg::Tensor { data, shape, dtype } => Arg::Tensor {
                data: data.iter().map(|x| x * 0.5 + 0.01).collect(),
                shape: shape.clone(),
                dtype: *dtype,
            },
            other => other.clone(),
        }
    };
    let input_name = "t0"; // manifest: id 0, role `embed` — the per-step input.
    let refs_a: Vec<(&str, Arg)> = args.iter().map(|(n, a)| (n.as_str(), a.clone())).collect();
    let refs_b: Vec<(&str, Arg)> = args
        .iter()
        .map(|(n, a)| {
            (
                n.as_str(),
                if n == input_name {
                    perturb(a)
                } else {
                    a.clone()
                },
            )
        })
        .collect();
    let input_b: Vec<(&str, Arg)> = refs_b
        .iter()
        .filter(|(n, _)| *n == input_name)
        .map(|(n, a)| (*n, a.clone()))
        .collect();
    assert!(!input_b.is_empty(), "input source t0 not found in args");

    // FRESH executor with input B — the reference for the perturbed input.
    let mut fresh =
        ktir_emulator::resident::ResidentExecutor::new(p.module.clone(), &p.spec).unwrap();
    fresh.set_sources(&refs_b).unwrap();
    let reference = read(&fresh.run(&[&key]).unwrap());

    // REUSED executor (the decode-loop shape): full sources with input A + run to warm
    // the weight cache, then a PARTIAL set_sources with ONLY the new input B + run. The
    // constant weights stay resident across the partial update; the result MUST equal
    // the fresh executor's. A stale cached weight (or KV buffer) makes it diverge.
    let mut reused =
        ktir_emulator::resident::ResidentExecutor::new(p.module.clone(), &p.spec).unwrap();
    reused.set_sources(&refs_a).unwrap();
    let _ = reused.run(&[&key]).unwrap(); // warm the weight cache
    reused.set_sources(&input_b).unwrap(); // partial update — only the input changes
    let step2 = read(&reused.run(&[&key]).unwrap());

    let max_abs = reference
        .iter()
        .zip(&step2)
        .map(|(a, b)| (a - b).abs())
        .fold(0.0f32, f32::max);
    assert_eq!(reference.len(), step2.len(), "length mismatch");
    eprintln!("multistep partial set_sources: max-abs vs fresh = {max_abs:.6}");
    assert!(
        max_abs < 1e-3,
        "multi-step decode diverged from fresh by {max_abs} — the weight cache served \
         a stale/wrong buffer across set_sources (the bug the one-shot golden can't see)"
    );

    // PERF: the constant model weights must STAY RESIDENT across per-step set_sources
    // (the decode-loop shape) — NOT re-decode every token (the 2x regression). Measure
    // a FULL cold reload (misses on a pass that re-decodes everything) vs the per-step
    // misses (which should only be the few forward-written KV operands), and assert the
    // per-step count is a small FRACTION of the full reload — i.e. most weights kept.
    use std::sync::atomic::Ordering;
    let misses = || ktir_emulator::metal::WEIGHT_CACHE_MISSES.load(Ordering::Relaxed);
    // A full reload: clear everything, then one pass repopulates EVERY cached weight.
    ktir_emulator::metal::clear_weight_cache();
    let m0 = misses();
    let _ = reused.run(&[&key]).unwrap();
    let full_reload = misses() - m0;
    // Per-step updates: only the input changes; constant weights must stay resident.
    let mut steps = 0usize;
    let m1 = misses();
    for i in 0..4 {
        let inp: Vec<(&str, Arg)> = args
            .iter()
            .filter(|(n, _)| n == input_name)
            .map(|(n, a)| (n.as_str(), if i % 2 == 0 { perturb(a) } else { a.clone() }))
            .collect();
        reused.set_sources(&inp).unwrap();
        let _ = reused.run(&[&key]).unwrap();
        steps += 1;
    }
    let per_step = (misses() - m1) / steps.max(1);
    eprintln!(
        "decode residency: {per_step} weight-cache misses/step vs {full_reload} on a full reload \
         ({}% kept resident)",
        100 - (per_step * 100 / full_reload.max(1))
    );
    // Keeping the constant weights resident leaves only the few forward-written (KV)
    // operands to re-read — well under half a full reload. (Before the fix this was a
    // FULL reload every step — the 2x decode regression.)
    assert!(
        per_step * 2 < full_reload,
        "weights are NOT staying resident across set_sources ({per_step}/step vs {full_reload} \
         full reload) — the per-token re-decode regression is back"
    );
}

