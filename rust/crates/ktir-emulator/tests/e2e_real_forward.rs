/// Resolve a vendored fixture to a directory of files (`manifest.json`,
/// `node*.mlir`, `t<id>.f16.gz`, `golden.f16.gz`).
///
/// Fixtures are committed as one `tests/fixtures/<name>.tar.gz` per model×config
/// (4 archives instead of the ~1.6k loose dump files that bloated the diff). This
/// unpacks the archive ONCE into the cargo target tmp dir and hands back that
/// path, so every downstream read (`dir.join(...)`) is unchanged. An already
/// unpacked `tests/fixtures/<name>/` — e.g. freshly written by `gen_golden.py`,
/// or `tar xzf`'d for local work — is preferred as-is (and is gitignored).
fn fixture_dir(name: &str) -> PathBuf {
    static CACHE: OnceLock<Mutex<HashMap<String, PathBuf>>> = OnceLock::new();
    let mut cache = CACHE
        .get_or_init(|| Mutex::new(HashMap::new()))
        .lock()
        .unwrap();
    if let Some(dir) = cache.get(name) {
        return dir.clone();
    }

    let fixtures = Path::new(env!("CARGO_MANIFEST_DIR")).join("tests/fixtures");
    let unpacked = fixtures.join(name);
    let dir = if unpacked.join("manifest.json").is_file() {
        unpacked
    } else {
        // Unpack into the (per-target, cargo-managed) tmp dir, once. Re-run /
        // parallel safe: a fully populated `dest` (manifest present) is reused;
        // otherwise we extract into a unique temp dir and ATOMICALLY rename it
        // into place. We never mutate `dest` in situ — so a second test run, a
        // concurrent `cargo test`, or a crashed prior extraction can't observe a
        // half-written fixture or clobber one another.
        // `CARGO_TARGET_TMPDIR` (set for integration tests) is a compile-time var,
        // so read it with `env!`, not at runtime — keeps extraction under
        // `target/tmp/` (cleaned by `cargo clean`) rather than leaking to /tmp.
        let base = Path::new(env!("CARGO_TARGET_TMPDIR")).join("ktir-fixtures");
        let dest = base.join(name);
        if !dest.join("manifest.json").is_file() {
            let archive = fixtures.join(format!("{name}.tar.gz"));
            let file = std::fs::File::open(&archive)
                .unwrap_or_else(|e| panic!("open fixture archive {archive:?}: {e}"));
            let tmp = base.join(format!(".incoming-{name}-{}", std::process::id()));
            let _ = std::fs::remove_dir_all(&tmp);
            std::fs::create_dir_all(&tmp).unwrap();
            tar::Archive::new(flate2::read::GzDecoder::new(file))
                .unpack(&tmp)
                .unwrap_or_else(|e| panic!("unpack {archive:?}: {e}"));
            // Publish atomically. If another writer won the race, `dest` is
            // already complete (rename onto a non-empty dir fails) — drop our temp.
            if std::fs::rename(&tmp, &dest).is_err() {
                let _ = std::fs::remove_dir_all(&tmp);
            }
        }
        dest
    };
    cache.insert(name.to_string(), dir.clone());
    dir
}

/// Hermetic (no network, no GPU): every vendored fixture archive unpacks and
/// every `node*.mlir` it references parses. Guards the tar.gz vendoring scheme
/// itself, independent of the weight-fetching real-forward tests below.
#[test]
fn fixtures_unpack_and_parse() {
    for name in [
        "smollm2-135m",
        "smollm2-135m-prefill",
        "llama-3.2-1b",
        "llama-3.2-1b-prefill",
    ] {
        let dir = fixture_dir(name);
        assert!(
            dir.join("golden.f16.gz").is_file(),
            "{name}: golden.f16.gz missing after unpack"
        );
        // Parses manifest.json + every node*.mlir; panics on any corruption.
        let _ = load_program(&dir);
    }
}

/// Read a gzip'd, headerless, row-major little-endian **f16** `.f16.gz` (the
/// vendored golden / runtime-input convention written by `gen_golden.py`) and
/// widen to f32. f16 because Spyre runs f16 (the program is f16, so f32 storage is
/// needless and the `max_abs` band is looser than f16); gzip -9 because the
/// constant tensors (zero KV-prefix, identity cos/sin, all-masked mask) compress to
/// ~nothing.
fn read_f16_gz(path: &Path) -> Vec<f32> {
    use std::io::Read;
    let file = std::fs::File::open(path).unwrap_or_else(|e| panic!("open {path:?}: {e}"));
    let mut bytes = Vec::new();
    flate2::read::GzDecoder::new(file)
        .read_to_end(&mut bytes)
        .unwrap_or_else(|e| panic!("gunzip {path:?}: {e}"));
    ktir_emulator::codec::decode(&bytes, bytes.len() / 2, DType::F16)
}

/// CI runs all smollm2 tests + the llama PREFILL golden only. The other heavy llama
/// tests self-skip when `CI` is set (GitHub Actions sets it automatically), since each
/// takes ~100-270s on the GPU-less CI runner for coverage smollm2 already gives. Unset
/// in a normal local shell ⇒ everything runs.
fn skip_heavy_llama_in_ci() -> bool {
    if std::env::var_os("CI").is_some() {
        eprintln!("CI set — skipping heavy llama test (smollm2 covers this path)");
        return true;
    }
    false
}

fn argmax(v: &[f32]) -> usize {
    v.iter()
        .enumerate()
        .fold((0, f32::NEG_INFINITY), |(bi, bv), (i, &x)| {
            if x > bv { (i, x) } else { (bi, bv) }
        })
        .0
}

/// A vendored program: module + spec + the metadata to marshal sources.
struct Program {
    module: IRModule,
    spec: ProgramSpec,
    /// tensor id -> (rows, cols)
    shape: HashMap<u64, (usize, usize)>,
    /// source id -> (role, optional HF disk-name) from manifest `sources[]`
    src_meta: HashMap<u64, (String, Option<String>)>,
    is_source: HashSet<u64>,
    mask_id: Option<u64>,
    result_id: u64,
}

fn load_program(dir: &Path) -> Program {
    let manifest: serde_json::Value =
        serde_json::from_slice(&std::fs::read(dir.join("manifest.json")).unwrap()).unwrap();

    let mut shape = HashMap::new();
    let mut is_source = HashSet::new();
    for t in manifest["tensors"].as_array().unwrap() {
        let id = t["id"].as_u64().unwrap();
        shape.insert(
            id,
            (
                t["rows"].as_u64().unwrap() as usize,
                t["cols"].as_u64().unwrap() as usize,
            ),
        );
        if t["is_source"].as_bool().unwrap_or(false) {
            is_source.insert(id);
        }
    }
    let mut src_meta = HashMap::new();
    for s in manifest["sources"].as_array().unwrap() {
        let id = s["id"].as_u64().unwrap();
        let role = s["role"].as_str().unwrap_or("weight").to_string();
        let disk = s["disk"].as_str().map(|x| x.to_string());
        src_meta.insert(id, (role, disk));
    }
    let result_id = manifest["result"].as_u64().unwrap();
    let mask_id = manifest["attn_mask"].as_u64();

    let mut sources: HashSet<u64> = is_source.clone();
    if let Some(m) = mask_id {
        sources.insert(m);
    }

    let mut module = IRModule::default();
    let mut nodes = Vec::new();
    for node in manifest["nodes"].as_array().unwrap() {
        let func = node["fn"].as_str().unwrap().to_string();
        let mlir = node["mlir"].as_str().unwrap();
        let parsed = parse_module(&std::fs::read_to_string(dir.join(mlir)).unwrap())
            .unwrap_or_else(|e| panic!("parse {mlir}: {e}"));
        for (_, f) in parsed.functions {
            module.add_function(f);
        }
        let bindings = node["args"]
            .as_array()
            .unwrap()
            .iter()
            .map(|a| Binding {
                arg: format!("%{}", a["name"].as_str().unwrap()),
                tensor: a["tensor"].as_u64().unwrap(),
                is_output: a["is_output"].as_bool().unwrap_or(false),
            })
            .collect();
        nodes.push(NodeSpec { func, bindings });
    }
    let spec = ProgramSpec {
        nodes,
        sources,
        results: HashSet::from([result_id]),
    };
    Program {
        module,
        spec,
        shape,
        src_meta,
        is_source,
        mask_id,
        result_id,
    }
}

